        ]
    ];

    $lang = app_lang();
    $text = $translations[$lang][$key] ?? ($translations['en'][$key] ?? $key);
    foreach ($vars as $varKey => $varValue) {
        $text = str_replace('{{' . $varKey . '}}', (string)$varValue, $text);
