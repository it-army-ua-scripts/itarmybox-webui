            'placeholder_percent_default_0' => '0..100 (за замовчуванням: 0)',
            'placeholder_digits_default_8000' => 'лише цифри (за замовчуванням: 8000)',
            'placeholder_digits_default_4096' => 'лише цифри (за замовчуванням: 4096)',
