function app_lang(): string
{
    static $lang = null;
    if ($lang !== null) {
        return $lang;
    }

    $allowed = ['en', 'uk'];
    $requested = $_GET['lang'] ?? ($_COOKIE['lang'] ?? 'uk');
    if (!in_array($requested, $allowed, true)) {
        $requested = 'uk';
    }

    if (!isset($_COOKIE['lang']) || $_COOKIE['lang'] !== $requested) {
        setcookie('lang', $requested, [
            'expires' => time() + 31536000,
            'path' => '/',
            'samesite' => 'Lax',
        ]);
    }

    $lang = $requested;
    return $lang;
}

